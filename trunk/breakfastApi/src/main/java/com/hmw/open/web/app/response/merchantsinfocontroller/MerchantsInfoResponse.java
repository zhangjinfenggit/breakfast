package com.hmw.open.web.app.response.merchantsinfocontroller;

import com.baomidou.mybatisplus.annotations.TableField;

/***
 * 商家信息返回
 * @author kongliufeng
 *
 */
public class MerchantsInfoResponse {
	/**
	 * 门脸照
	 */
	private String photo;
	
	/**
	 * 店铺名称
	 */
	private String name;
	
	/**
	 * 手机号
	 */
	private String phone;
	
	/**
	 * 主键id
	 */
	private Long id;

	/**
	 * 详细地址
	 */
	private String address;
	
	/**
	 * 店铺位置
	 */
	private String position;

	/**
	 * 银行卡号（也是收款卡号）
	 */
	@TableField(value="card_no")
	private String cardNo;
	
	/**
	 * 认证情况：1、已认证（绿色） 2、未认证（红色）
	 */
	@TableField(value="certified_status")
	private String certifiedStatus;

	public String getPhoto() {
		return photo;
	}

	public void setPhoto(String photo) {
		this.photo = photo;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	
	
	public String getPosition() {
		return position;
	}

	public void setPosition(String position) {
		this.position = position;
	}

	public String getCardNo() {
		return cardNo;
	}

	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}

	public String getCertifiedStatus() {
		return certifiedStatus;
	}

	public void setCertifiedStatus(String certifiedStatus) {
		this.certifiedStatus = certifiedStatus;
	}
	
	
}
